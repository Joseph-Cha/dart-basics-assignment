## 2. 전체 구매 예정 금액에 따른 할인 여부를 확인하는 기능

[ 설명 ] 
- 1번에서 구한 상품들의 총 가격에 따라 할인 여부를 계산하여 출력합니다.

[ 조건 ]
- 상품들의 총 가격이 2만원이 넘은 경우
  - `할인 금액 : [1번에서 구한 가격의 10%]원` 을 출력합니다.
      
      ```dart
        장바구니에 35000원 어치를 담으셨네요 ! 
