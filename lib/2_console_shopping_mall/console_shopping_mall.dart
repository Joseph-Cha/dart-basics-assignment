## 1. 구매자가 장바구니에 담은 상품 목록을 볼 수 있는 기능 
[ 설명 ]
구매자의 장바구니에 담겨 있는 물품들의 전체 금액을 계산합니다.

[ 조건 ]
현재 가게에서 판매하고 있는 상품들과 각각의 가격은 다음과 같습니다.
티셔츠 : 10000원
바지 : 8000원
모자 : 4000원 

* 구매자의 장바구니 품목들이 아래 리스트와 같다면, 물품의 전체 금액을 계산하여 출력하세요!
```dart
List<String> cart = ["티셔츠", "바지", "모자", "티셔츠", "바지"];​
